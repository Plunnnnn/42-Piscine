#include <unistd.h>

void	ft_putstr(char *str)
{
	char	*ptr_dest;

	ptr_dest = str;
	while (*ptr_dest != '\0')
	{	
		write(1, &(*ptr_dest), 1);
		ptr_dest++;
	}
}
